class Colors:
    """ANSI escape codes for terminal text styling."""

    RESET = "\033[0m"

    # Text colors
    RED = "\033[91m"
    GREEN = "\033[92m"
    YELLOW = "\033[93m"
    BLUE = "\033[94m"
    MAGENTA = "\033[95m"
    CYAN = "\033[96m"
    WHITE = "\033[97m"

    # Bold
    BOLD = "\033[1m"
    DIM = "\033[2m"

    # Backgrounds (optional)
    BG_RED = "\033[41m"
    BG_GREEN = "\033[42m"
